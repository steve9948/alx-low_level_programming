#include "3-calc.h"

/**
 * op_add - Prints the sum of two numbers.
 * @a: First number.
 * @b: second number.
 *
 * Return: Sum of a and b.
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - prints the difference of two numbers.
 * @a: First number.
 * @b: Second number.
 *
 * Return: The difference of a and b.
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Prints the product of two numbers.
 * @a: First number.
 * @b: second number.
 *
 * Return: The product of a and b.
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Prints the division of two numbers.
 * @a: First number.
 * @b: Second number.
 *
 * Return: The quotient of a and b.
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Prints the remainder of the division of two numbers.
 * @a: First number.
 * @b: Second number.
 *
 * Return: The remainder of the division of int a and b.
 */
int op_mod(int a, int b)
{
	return (a % b);
}
